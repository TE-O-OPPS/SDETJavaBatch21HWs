package org.example.project.homeworkTekiaJavaCodingAssignment;

public class GroupHomeWork2 {
    public static void main(String[] args) {
        int[] numbers = {10, 20, 30, 40};
        int numberSum=0;
        for(int num:numbers){
            numberSum+=num;
        }
        System.out.println(numberSum);
    }
}